% предикат app(L1, L2, L3), който конкатенира L1 & L2 и резултата е в L3
app([], L2, L2).
app([H|T], L2, [H|L3]) :- app(T, L2, L3).

% предикат mem(X, L), който проверява дали X е член на L
mem(X, L) :- app(_, [X|_], L).
mem_unique(X, L) :- app(_, [X|L2], L), not(mem(X, L2)).